package trabajopractico4;

/**
 *
 * @author Bruno
 */
public class Instancia {
    private int experimento; 
    private double rndRecuerdaMensaje;
    private String recuerdaMensaje;
    private double rndCompraProducto;
    private String comprarProducto;
    private int sumatoriaCompras;

    public Instancia() {
        
    }

    public int getExperimento() {
        return experimento;
    }

    public void setExperimento(int experimento) {
        this.experimento = experimento;
    }

    public double getRndRecuerdaMensaje() {
        return rndRecuerdaMensaje;
    }

    public void setRndRecuerdaMensaje(double rndRecuerdaMensaje) {
        this.rndRecuerdaMensaje = rndRecuerdaMensaje;
    }

    public String getRecuerdaMensaje() {
        return recuerdaMensaje;
    }

    public void setRecuerdaMensaje(String recuerdaMensaje) {
        this.recuerdaMensaje = recuerdaMensaje;
    }

    public double getRndCompraProducto() {
        return rndCompraProducto;
    }

    public void setRndCompraProducto(double rndCompraProducto) {
        this.rndCompraProducto = rndCompraProducto;
    }

    public String getComprarProducto() {
        return comprarProducto;
    }

    public void setComprarProducto(String comprarProducto) {
        this.comprarProducto = comprarProducto;
    }

    public int getSumatoriaCompras() {
        return sumatoriaCompras;
    }

    public void setSumatoriaCompras(int sumatoriaCompras) {
        this.sumatoriaCompras = sumatoriaCompras;
    }
    
    
}
